#!/usr/bin/env node
'use strict';
/**
 * Generates Mobile's static language artifacts from the on-disk languages/ tree.
 * Metro cannot read language files dynamically, so loadLanguageFile.ts (a static
 * require() map) and languageData.ts (languages / levels / availableCombinations /
 * LANGUAGE_META) are generated here and committed.
 *
 * Run: npm run generate:languages
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const LANGUAGES_DIR = path.join(ROOT, 'languages');
const CODE_LEVELS = ['a1', 'a2', 'b1', 'b2', 'c1'];
const UI_LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1'];

// Canonical code -> { name, flag }. Mirror of Desktop src/shared/constants.ts LANGUAGE_META.
const META = {
  en: { name: 'English', flag: '🇬🇧' },
  de: { name: 'German', flag: '🇩🇪' },
  fr: { name: 'French', flag: '🇫🇷' },
  ru: { name: 'Russian', flag: '🇷🇺' },
  es: { name: 'Spanish', flag: '🇪🇸' },
  it: { name: 'Italian', flag: '🇮🇹' },
  tr: { name: 'Turkish', flag: '🇹🇷' },
};

function fail(msg) {
  console.error(`[generate-language-map] ERROR: ${msg}`);
  process.exit(1);
}
function isDir(p) {
  return fs.existsSync(p) && fs.statSync(p).isDirectory();
}

// Discover pairs: languages/<toCode>/<fromCode>/<level>.json
const toCodes = fs
  .readdirSync(LANGUAGES_DIR)
  .filter((n) => !n.startsWith('_') && isDir(path.join(LANGUAGES_DIR, n)))
  .sort();

const requireEntries = []; // { key, rel }
const combos = {}; // learningName -> Set(knownName)
const presentCodes = new Set();

for (const toCode of toCodes) {
  if (!META[toCode]) fail(`unknown language code "${toCode}" (not in META)`);
  const toDir = path.join(LANGUAGES_DIR, toCode);
  const fromCodes = fs
    .readdirSync(toDir)
    .filter((n) => !n.startsWith('_') && isDir(path.join(toDir, n)))
    .sort();
  for (const fromCode of fromCodes) {
    if (!META[fromCode]) fail(`unknown language code "${fromCode}" (not in META)`);
    const fromDir = path.join(toDir, fromCode);
    const levels = CODE_LEVELS.filter((lvl) =>
      fs.existsSync(path.join(fromDir, `${lvl}.json`)),
    );
    if (levels.length === 0) continue; // skip dirs with only custom.json etc.
    const learning = META[toCode].name;
    const known = META[fromCode].name;
    presentCodes.add(toCode);
    presentCodes.add(fromCode);
    if (!combos[learning]) combos[learning] = new Set();
    combos[learning].add(known);
    for (const lvl of levels) {
      const rel = `../../languages/${toCode}/${fromCode}/${lvl}.json`;
      const abs = path.join(ROOT, 'app', 'utils', rel);
      if (!fs.existsSync(abs)) fail(`generated require path does not resolve: ${rel}`);
      requireEntries.push({
        key: `${learning.toLowerCase()}-${known.toLowerCase()}-${lvl}`,
        rel,
      });
    }
  }
}

if (requireEntries.length === 0) fail('no language pairs found');

const languageNames = [...presentCodes]
  .map((c) => META[c].name)
  .sort((a, b) => a.localeCompare(b));

const availableCombinations = {};
for (const name of languageNames) {
  const set = combos[name] || new Set();
  availableCombinations[name] = [...set].sort((a, b) => a.localeCompare(b));
}

const metaByName = {};
for (const name of languageNames) {
  const code = Object.keys(META).find((c) => META[c].name === name);
  metaByName[name] = { code, flag: META[code].flag };
}

requireEntries.sort((a, b) => a.key.localeCompare(b.key));

const HEADER =
  '// DO NOT EDIT — generated by scripts/generate-language-map.js\n' +
  '// Run `npm run generate:languages` to regenerate.\n';

// ---- loadLanguageFile.ts ----
const loaderBody = `${HEADER}import type { Word } from './types';

/**
 * Static require map. Metro requires literal \`require(...)\` calls, so the paths
 * stay inline; keyed by \`"\${learning}-\${known}-\${level}"\` (full lowercase
 * language names) -> the code-path JSON file.
 */
const LANGUAGE_FILES: Record<string, Word[]> = {
${requireEntries.map((e) => `  '${e.key}': require('${e.rel}'),`).join('\n')}
};

/** Same fallback as the legacy switch's \`default\` branch. */
const FALLBACK: Word[] = require('../../languages/en/de/c1.json');

/**
 * Loads the word list for the given learning/known language pair and level.
 * Async to preserve the previous call site's contract.
 */
const loadLanguageFile = async (
  learning: string,
  known: string,
  level: string,
): Promise<Word[]> => {
  const key = \`\${learning}-\${known}-\${level}\`;
  return LANGUAGE_FILES[key] ?? FALLBACK;
};

export default loadLanguageFile;
`;

// ---- languageData.ts ----
const dataBody = `${HEADER}
export const languages = [
${languageNames.map((n) => `  '${n}',`).join('\n')}
] as const;

export type Language = (typeof languages)[number];

export const levels = [${UI_LEVELS.map((l) => `'${l}'`).join(', ')}] as const;

export interface LanguageMeta {
  code: string;
  flag: string;
}

export const availableCombinations: Record<Language, Language[]> = {
${languageNames
  .map((n) => `  '${n}': [${availableCombinations[n].map((k) => `'${k}'`).join(', ')}],`)
  .join('\n')}
};

export const LANGUAGE_META: Record<Language, LanguageMeta> = {
${languageNames
  .map((n) => `  '${n}': { code: '${metaByName[n].code}', flag: '${metaByName[n].flag}' },`)
  .join('\n')}
};
`;

fs.writeFileSync(path.join(ROOT, 'app', 'utils', 'loadLanguageFile.ts'), loaderBody);
fs.writeFileSync(path.join(ROOT, 'app', 'utils', 'languageData.ts'), dataBody);

console.log(
  `[generate-language-map] OK: ${requireEntries.length} files, ${languageNames.length} languages.`,
);
